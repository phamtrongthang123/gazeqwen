"""
GazeLens data pipeline.

Provides:
  - ScanpathLoader: reads _fixation_filtered.csv for a video, returns (N, 4) tensor
  - GazeLensDataset: loads temporal task QA JSONs + maps to scanpath CSVs
  - collate_fn: handles variable-length scanpaths (pad to max in batch)

QA JSON format (per entry):
  {
    "video_path": "...",
    "response_time": "[MM:SS - MM:SS]",
    "questions": [
      {
        "question": "...",
        "time_stamp": "MM:SS",
        "answer": "A",
        "options": ["A. ...", "B. ...", "C. ...", "D. ..."],
        "task_type": "...",
        ...task-specific fields...
      }
    ]
  }

Fixation CSV fields (step 1.5 output):
  fixation_id, center_x, center_y, start_time_seconds, end_time_seconds, duration, ...

Task-to-clip mapping (per spec.md §3.3):
  - Past tasks (OTP, GSM, SR, NFI): [0, question_time]
  - FAP (future task):               [max(0, question_time - 60), question_time]
"""

import os
import csv
import json
import math
from pathlib import Path
from typing import List, Optional, Tuple, Dict, Any

import torch
from torch.utils.data import Dataset


# ---------------------------------------------------------------------------
# Task type registry
# ---------------------------------------------------------------------------

# Maps task_type string -> clip window strategy
#   "past"   = [0, question_time]
#   "window" = [max(0, question_time - 60), question_time]
#
# Two naming conventions exist in the wild:
#   Pipeline CamelCase: generated by qa_generation/*.py (e.g. "Object_Identification")
#   Benchmark snake_case: finalized QA files (e.g. "present_object_identification_easy")
# Both are listed so GazeLens handles QA JSONs from either source without misassigning
# clip windows (present tasks must use "window", not the "past" fallback).
_TASK_CLIP_STRATEGY: Dict[str, str] = {
    # ── Past tasks ──────────────────────────────────────────────────────────
    # Benchmark canonical names
    "past_object_transition_prediction": "past",       # OTP
    "past_gaze_sequence_matching": "past",             # GSM
    "past_scene_recall": "past",                       # SR
    "past_non_fixated_object_identification": "past",  # NFI
    # Pipeline CamelCase names (qa_generation/past.py)
    "Scanpath_Next_After_Group": "past",               # OTP / GSM
    "Scanpath_Transition_Pattern": "past",             # OTP variant
    "Scanpath_Never_Gazed": "past",                    # NFI
    "Scene_Reconstruction": "past",                    # SR
    # Lowercase aliases
    "scene_recall": "past",
    "scanpath_reasoning": "past",
    # ── Present tasks ────────────────────────────────────────────────────────
    # Benchmark canonical names
    "present_object_identification_easy": "window",    # OI-Easy
    "present_object_identification_hard": "window",    # OI-Hard
    "present_object_attribute_recognition": "window",  # OAR
    # Pipeline CamelCase names (qa_generation/present.py)
    "Object_Identification": "window",                 # OI (both easy and hard)
    "Object_Attribute": "window",                      # OAR
    # Lowercase aliases (legacy)
    "object_identification": "window",
    "object_attribute_recognition": "window",
    # ── Present / future tasks ───────────────────────────────────────────────
    # Benchmark canonical name
    "present_future_action_prediction": "window",      # FAP
    # Pipeline CamelCase names (qa_generation/future.py)
    "Future_Action_Prediction": "window",              # FAP
    "Object_Remind_Easy": "window",                    # object remind (future)
    "Object_Remind_Hard": "window",                    # object remind (future)
    # Lowercase aliases
    "future_action_prediction": "window",
    "FAP": "window",
    # ── Proactive tasks ─────────────────────────────────────────────────────
    "proactive_gaze_triggered_alert": "proactive",   # GTA
    "proactive_object_appearance_alert": "proactive", # OAA
}

# Tasks treated as "past" if task_type is missing
_DEFAULT_CLIP_STRATEGY = "past"

# Canonical task names for grouping
TASK_PAST = {"OTP", "GSM", "SR", "NFI"}
TASK_FUTURE = {"FAP"}
TASK_PRESENT = {"OI-Easy", "OI-Hard", "OAR"}


def _parse_timestamp(ts: str) -> float:
    """Convert 'MM:SS' or 'HH:MM:SS' or float-string to seconds."""
    parts = ts.strip().split(":")
    if len(parts) == 2:
        return int(parts[0]) * 60 + int(parts[1])
    elif len(parts) == 3:
        return int(parts[0]) * 3600 + int(parts[1]) * 60 + int(parts[2])
    else:
        return float(ts)


def _clip_strategy_for_question(q: Dict[str, Any]) -> str:
    """Return 'past' or 'window' based on the question's task_type."""
    task_type = q.get("task_type", "")
    if task_type in _TASK_CLIP_STRATEGY:
        return _TASK_CLIP_STRATEGY[task_type]
    # Fallback: check required_ability
    required = q.get("required_ability", "")
    if "future" in task_type.lower() or "future" in required.lower():
        return "window"
    return _DEFAULT_CLIP_STRATEGY


# ---------------------------------------------------------------------------
# ScanpathLoader
# ---------------------------------------------------------------------------

class ScanpathLoader:
    """
    Loads fixation data from a _fixation_filtered.csv file.

    Expected CSV columns (at minimum):
      fixation_id, center_x, center_y, start_time_seconds, end_time_seconds

    Optional columns used if present:
      duration

    Returns:
      (N, 4) float tensor: [center_x, center_y, midpoint_time, duration]
      sorted by fixation_id (or start_time_seconds if fixation_id absent).
    """

    def __init__(self, csv_path: str):
        self.csv_path = csv_path
        self._data: Optional[torch.Tensor] = None  # cached

    def load(self) -> torch.Tensor:
        """Load and return (N, 4) tensor. Cached after first call."""
        if self._data is not None:
            return self._data
        self._data = self._read_csv(self.csv_path)
        return self._data

    def load_clipped(self, start_time: float, end_time: float) -> torch.Tensor:
        """
        Load scanpath clipped to [start_time, end_time].

        Returns (M, 4) tensor where M <= N. If no fixations in window,
        returns a zero-row tensor of shape (0, 4).
        """
        data = self.load()
        if data.shape[0] == 0:
            return data
        # Midpoint time is column index 2
        t = data[:, 2]
        mask = (t >= start_time) & (t <= end_time)
        return data[mask]

    @staticmethod
    def _read_csv(path: str) -> torch.Tensor:
        """
        Parse the fixation CSV and return a (N, 4) float tensor.

        Handles:
          - Missing 'duration' column → set duration=0
          - Missing 'fixation_id' column → sort by start_time_seconds
          - 'start_time' / 'end_time' as aliases for start/end_time_seconds
        """
        rows: List[Tuple[float, float, float, float, float]] = []
        # (fixation_id_for_sort, x, y, midpoint_t, dur)

        try:
            with open(path, newline="", encoding="utf-8") as f:
                reader = csv.DictReader(f)
                fieldnames = reader.fieldnames or []

                # Normalize column aliases
                has_start = "start_time_seconds" in fieldnames or "start_time" in fieldnames
                has_end = "end_time_seconds" in fieldnames or "end_time" in fieldnames
                has_dur = "duration" in fieldnames
                has_fid = "fixation_id" in fieldnames

                start_col = "start_time_seconds" if "start_time_seconds" in fieldnames else "start_time"
                end_col = "end_time_seconds" if "end_time_seconds" in fieldnames else "end_time"

                for row in reader:
                    try:
                        x = float(row["center_x"])
                        y = float(row["center_y"])
                        t_start = float(row[start_col]) if has_start else 0.0
                        t_end = float(row[end_col]) if has_end else t_start
                        mid_t = (t_start + t_end) / 2.0
                        dur = float(row["duration"]) if has_dur else (t_end - t_start)
                        fid = float(row["fixation_id"]) if has_fid else t_start
                        rows.append((fid, x, y, mid_t, dur))
                    except (ValueError, KeyError):
                        continue  # skip malformed rows

        except FileNotFoundError:
            return torch.zeros(0, 4)
        except Exception:
            return torch.zeros(0, 4)

        if not rows:
            return torch.zeros(0, 4)

        # Sort by fixation_id (or start_time if no fixation_id)
        rows.sort(key=lambda r: r[0])

        # Build tensor: [x, y, mid_t, dur]
        arr = torch.tensor([[r[1], r[2], r[3], r[4]] for r in rows], dtype=torch.float32)
        return arr


