class GazeLensDataset(Dataset):
    """
    Dataset over temporal task QA entries.

    Each item is a flat (one question) sample:
      {
        "video_path":     str,
        "scanpath":       (N, 4) float tensor  [x, y, t, dur]  — clipped to clip window
        "question_text":  str,
        "options":        List[str],  e.g. ["A. ...", "B. ...", ...]
        "answer":         str,        single letter "A"/"B"/"C"/"D"
        "answer_idx":     int,        0/1/2/3
        "start_time":     float,      clip start (seconds)
        "end_time":       float,      clip end   (seconds)
        "question_time":  float,      question timestamp (seconds)
        "task_type":      str,
      }

    Args:
        qa_files:        list of paths to QA JSON files
        video_root:      root directory to resolve relative video paths
        fixation_root:   root directory to search for *_fixation_filtered.csv files
                         (if None, searches alongside the video)
        split:           "train"/"val"/"test" or None (use all)
        split_file:      optional JSON {"train": [...], "val": [...], "test": [...]}
                         where values are video basename lists
        min_fixations:   skip samples where scanpath has fewer than this many fixations
    """

    def __init__(
        self,
        qa_files: List[str],
        video_root: str = "",
        fixation_root: Optional[str] = None,
        split: Optional[str] = None,
        split_file: Optional[str] = None,
        min_fixations: int = 1,
    ):
        self.video_root = video_root
        self.fixation_root = fixation_root
        self.min_fixations = min_fixations

        # Load split whitelist if provided
        split_videos: Optional[set] = None
        if split_file and split and os.path.exists(split_file):
            with open(split_file) as f:
                splits = json.load(f)
            split_videos = set(splits.get(split, []))

        # Parse all QA entries into flat samples
        self.samples: List[Dict[str, Any]] = []
        self._scanpath_cache: Dict[str, ScanpathLoader] = {}

        for qa_path in qa_files:
            entries = self._load_qa_json(qa_path)
            for entry in entries:
                video_path = self._resolve_video_path(entry.get("video_path", ""))
                video_name = os.path.basename(video_path)

                # Apply split filter
                if split_videos is not None and video_name not in split_videos:
                    continue

                inferred_task_type = Path(qa_path).stem
                is_proactive = _TASK_CLIP_STRATEGY.get(
                    inferred_task_type, _DEFAULT_CLIP_STRATEGY
                ) == "proactive"

                for q in entry.get("questions", []):
                    if not q.get("task_type"):
                        q = {**q, "task_type": inferred_task_type}

                    if is_proactive:
                        for s in self._build_proactive_samples(q, video_path):
                            self.samples.append(s)
                    else:
                        sample = self._build_sample(entry, q, video_path)
                        if sample is not None:
                            self.samples.append(sample)

    # ------------------------------------------------------------------
    # Dataset protocol
    # ------------------------------------------------------------------

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> Dict[str, Any]:
        sample = self.samples[idx].copy()
        video_path = sample["video_path"]
        start_time = sample["start_time"]
        end_time = sample["end_time"]

        # Load and clip scanpath
        loader = self._get_loader(video_path)
        scanpath = loader.load_clipped(start_time, end_time)

        if scanpath.shape[0] < self.min_fixations:
            # Return empty scanpath (training loop should handle this gracefully)
            scanpath = torch.zeros(0, 4)

        sample["scanpath"] = scanpath
        return sample

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _load_qa_json(self, path: str) -> List[Dict[str, Any]]:
        try:
            with open(path) as f:
                data = json.load(f)
            if isinstance(data, list):
                return data
            return [data]
        except Exception:
            return []

    def _resolve_video_path(self, raw_path: str) -> str:
        """Prefer absolute path; fall back to video_root + basename."""
        if os.path.isabs(raw_path) and os.path.exists(raw_path):
            return raw_path
        name = os.path.basename(raw_path)
        candidate = os.path.join(self.video_root, name)
        return candidate  # May not exist; caller handles missing files

    def _get_loader(self, video_path: str) -> ScanpathLoader:
        """Return a cached ScanpathLoader for the given video."""
        if video_path not in self._scanpath_cache:
            csv_path = self._find_fixation_csv(video_path)
            self._scanpath_cache[video_path] = ScanpathLoader(csv_path)
        return self._scanpath_cache[video_path]

    def _find_fixation_csv(self, video_path: str) -> str:
        """
        Search for the fixation CSV associated with a video.

        Search order (for each root in fixation_root, which may be a
        space- or colon-separated list of paths):
          1. <root>/<video_stem>_fixation_filtered.csv
          2. <root>/<video_stem>/<video_stem>_fixation_filtered.csv
        Then falls back to the video directory.

        Returns the first path found, or a non-existent path if none found
        (ScanpathLoader handles missing files gracefully).
        """
        stem = Path(video_path).stem
        candidates = []

        if self.fixation_root:
            # Support space- or colon-separated list of roots
            import re as _re
            roots = _re.split(r'[:\s]+', self.fixation_root.strip())
            for root in roots:
                if root:
                    candidates.append(os.path.join(root, f"{stem}_fixation_filtered.csv"))
                    candidates.append(os.path.join(root, stem, f"{stem}_fixation_filtered.csv"))

        video_dir = os.path.dirname(video_path)
        if video_dir:
            candidates.append(os.path.join(video_dir, f"{stem}_fixation_filtered.csv"))

        for path in candidates:
            if os.path.exists(path):
                return path

        # Return default (non-existent) path; ScanpathLoader returns empty tensor
        return candidates[0] if candidates else f"{stem}_fixation_filtered.csv"

    def _build_sample(
        self,
        entry: Dict[str, Any],
        q: Dict[str, Any],
        video_path: str,
    ) -> Optional[Dict[str, Any]]:
        """
        Build a flat sample dict from an entry + question dict.
        Returns None if required fields are missing or invalid.
        """
        question_text = q.get("question", "").strip()
        if not question_text:
            return None

        # Parse question timestamp
        ts_str = q.get("time_stamp", "")
        if not ts_str:
            return None
        try:
            question_time = _parse_timestamp(ts_str)
        except (ValueError, IndexError):
            return None

        # Answer
        answer = q.get("answer", "").strip().upper()
        if answer not in ("A", "B", "C", "D"):
            return None
        answer_idx = ord(answer) - ord("A")

        # Options
        options = q.get("options", [])
        if not options:
            return None

        # Clip window
        strategy = _clip_strategy_for_question(q)
        if strategy == "past":
            start_time = 0.0
            end_time = question_time
        else:  # "window"
            start_time = max(0.0, question_time - 60.0)
            end_time = question_time

        task_type = q.get("task_type", "")

        return {
            "video_path": video_path,
            "question_text": question_text,
            "options": options,
            "answer": answer,
            "answer_idx": answer_idx,
            "start_time": start_time,
            "end_time": end_time,
            "question_time": question_time,
            "task_type": task_type,
        }

    def _build_proactive_samples(
        self,
        q: Dict[str, Any],
        video_path: str,
    ) -> List[Dict[str, Any]]:
        """Expand a proactive question into one MCQA sample per test_info point.

        Each test point becomes: A. Yes / B. No, with answer based on type (1=A, 0=B).
        The video clip is [0, realtime_seconds].
        """
        question_text = q.get("question", "").strip()
        if not question_text:
            return []

        task_type = q.get("task_type", "")
        test_info = q.get("test_info", [])
        if not test_info:
            return []

        # Wrap the original question in the alert-decision framing
        prompt_question = (
            f"{question_text} Based on the video so far, "
            f"should the alert be triggered?"
        )

        samples = []
        for ti in test_info:
            realtime = ti.get("realtime", "")
            ground_truth = ti.get("type")  # 0 or 1
            if ground_truth is None or not realtime:
                continue

            # Clip range
            clip = ti.get("input_video_clip")
            if clip:
                start_time = float(clip[0])
                end_time = float(clip[1])
            else:
                start_time = 0.0
                end_time = _parse_timestamp(realtime)

            if end_time <= start_time:
                end_time = start_time + 1.0

            # MCQA: A=Yes, B=No
            answer = "A" if ground_truth == 1 else "B"
            answer_idx = 0 if ground_truth == 1 else 1

            samples.append({
                "video_path": video_path,
                "question_text": prompt_question,
                "options": ["A. Yes", "B. No"],
                "answer": answer,
                "answer_idx": answer_idx,
                "start_time": start_time,
                "end_time": end_time,
                "question_time": end_time,
                "task_type": task_type,
            })

        return samples


# ---------------------------------------------------------------------------
# collate_fn
# ---------------------------------------------------------------------------

def collate_fn(
    batch: List[Dict[str, Any]],
) -> Dict[str, Any]:
    """
    Collate a list of GazeLensDataset samples into a batch.

    Variable-length scanpaths are padded with zeros to the max length in the
    batch. A 'scanpath_lengths' tensor records the true length of each sample.

    Returns dict with keys:
      scanpaths:         (B, N_max, 4) float tensor
      scanpath_lengths:  (B,) long tensor
      answer_idxs:       (B,) long tensor
      video_paths:       List[str]
      question_texts:    List[str]
      options:           List[List[str]]
      answers:           List[str]
      start_times:       List[float]
      end_times:         List[float]
      question_times:    List[float]
      task_types:        List[str]
    """
    lengths = [s["scanpath"].shape[0] for s in batch]
    max_len = max(lengths) if lengths else 1
    max_len = max(max_len, 1)  # avoid zero-length tensor

    B = len(batch)
    padded = torch.zeros(B, max_len, 4, dtype=torch.float32)
    for i, s in enumerate(batch):
        sp = s["scanpath"]
        n = sp.shape[0]
        if n > 0:
            padded[i, :n] = sp

    return {
        "scanpaths": padded,
        "scanpath_lengths": torch.tensor(lengths, dtype=torch.long),
        "answer_idxs": torch.tensor([s["answer_idx"] for s in batch], dtype=torch.long),
        "video_paths": [s["video_path"] for s in batch],
        "question_texts": [s["question_text"] for s in batch],
        "options": [s["options"] for s in batch],
        "answers": [s["answer"] for s in batch],
        "start_times": [s["start_time"] for s in batch],
        "end_times": [s["end_time"] for s in batch],
        "question_times": [s["question_time"] for s in batch],
        "task_types": [s["task_type"] for s in batch],
    }
